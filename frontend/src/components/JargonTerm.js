import { useState } from "react";
import JargonGlossary from "./JargonGlossary";

// Plain-language definitions for every jargon term used across the KeyForge UI.
// One short sentence per term, no other jargon nested inside.
// Keys are normalized to lower-case; lookups normalize the incoming term too.
