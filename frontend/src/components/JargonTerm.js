  mfa: "Multi-factor authentication: signing in requires both your password and a second code from your phone, so a stolen password alone is not enough.",
  kms: "A Key Management Service: a separate, hardened vault (such as one provided by AWS, GCP, or Azure) that protects the master key KeyForge uses to lock and unlock your stored credentials.",
  ttl: "Time-to-live: how long something is allowed to stay valid before it expires. A 3600-second TTL means it stops working one hour after it was created.",
  cidr: "A way to write a range of IP addresses as a single line, for example 192.168.1.0/24 means every address from 192.168.1.0 through 192.168.1.255.",
  "envelope encryption": "A two-layer locking system: each credential is locked with its own key, and that key is itself locked with a master key kept somewhere even safer. Compromising one credential cannot leak the others.",
  envelope: "A two-layer locking system: each credential is locked with its own key, and that key is itself locked with a master key kept somewhere even safer. Compromising one credential cannot leak the others.",
  dek: "Data Encryption Key: the inner key that actually scrambles a single credential's data. Each credential gets its own.",
