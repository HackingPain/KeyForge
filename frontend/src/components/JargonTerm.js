const lookup = (term) => {
  if (!term) return null;
  const key = String(term).toLowerCase().trim();
  if (JARGON[key]) return { key, definition: JARGON[key] };
  // Allow callers to pass already-uppercased acronyms or longer phrases; try collapsed match.
  const collapsed = key.replace(/\s+/g, " ");
  if (JARGON[collapsed]) return { key: collapsed, definition: JARGON[collapsed] };
  return null;
};

const JargonTerm = ({ term, children }) => {
  const [hovered, setHovered] = useState(false);
  const [focused, setFocused] = useState(false);
  const [glossaryOpen, setGlossaryOpen] = useState(false);
  const entry = lookup(term);

  // If we do not have a definition, render the children (or term) verbatim with no wrapper.
  if (!entry) {
    return <>{children ?? term}</>;
  }

  const visible = hovered || focused;
  const label = children ?? term;

  return (
    <>
      <span
        className="relative inline-block"
        onMouseEnter={() => setHovered(true)}
        onMouseLeave={() => setHovered(false)}
        onFocus={() => setFocused(true)}
        onBlur={() => setFocused(false)}
      >
        <span
          tabIndex={0}
          role="button"
          aria-describedby={`jargon-tip-${entry.key.replace(/\s+/g, "-")}`}
          className="border-b border-dotted border-gray-400 cursor-help focus:outline-none focus:ring-1 focus:ring-indigo-400 rounded-sm"
        >
          {label}
        </span>
        {visible && (
          <span
            id={`jargon-tip-${entry.key.replace(/\s+/g, "-")}`}
            role="tooltip"
            className="absolute z-50 left-0 top-full mt-1 w-72 p-3 bg-gray-900 text-white text-xs rounded-md shadow-lg normal-case font-normal"
          >
            <span className="block mb-2 leading-snug">{entry.definition}</span>
            <button
              type="button"
              onClick={(e) => {
                e.preventDefault();
                e.stopPropagation();
                setGlossaryOpen(true);
              }}
              className="text-indigo-300 hover:text-indigo-200 underline text-xs"
            >
              Learn more
            </button>
          </span>
        )}
      </span>
      {glossaryOpen && (
        <JargonGlossary
          isOpen={glossaryOpen}
          onClose={() => setGlossaryOpen(false)}
          initialTerm={entry.key}
        />
      )}
    </>
  );
};

export default JargonTerm;
